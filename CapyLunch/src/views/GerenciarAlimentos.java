    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        JFrame frame = new JFrame();
        
        int resposta = JOptionPane.showConfirmDialog(frame,"Você tem certeza de que quer salvar o Alimento?","Confirmação",JOptionPane.YES_NO_OPTION);
        
        if (resposta == JOptionPane.YES_OPTION) {
            String nome = txtNome.getText();
            float valor = Float.parseFloat(txtValor.getText());

            Alimento a = new Alimento(nome, valor);
            lista.add(a);

            DefaultTableModel modelo = (DefaultTableModel) tbAlimento.getModel();
            modelo.setNumRows(0);

            for (Alimento la : lista) {
                modelo.addRow(new Object[]{
                    la.getNome(),
                    la.getValor()
                });
            }

            txtNome.setText("");
            txtValor.setText("");
        } else {
            txtNome.setText("");
            txtValor.setText("");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed
    
