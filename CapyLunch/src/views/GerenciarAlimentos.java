    private void btDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeletarActionPerformed
        JFrame frame = new JFrame();
        
        int resposta = JOptionPane.showConfirmDialog(frame,"Você tem certeza de que quer deletar o Alimento?","Confirmação",JOptionPane.YES_NO_OPTION);
        
        if (resposta == JOptionPane.YES_OPTION) {
            int posicao = tbAlimento.getSelectedRow();
            lista.remove(posicao);

            DefaultTableModel modelo = (DefaultTableModel) tbAlimento.getModel();
            modelo.setNumRows(0);

            for (Alimento la : lista) {
                modelo.addRow(new Object[] {
                   la.getNome(),
                   la.getValor()
                });
            }

            txtNome.setText("");
            txtValor.setText("");
        } else {
            txtNome.setText("");
            txtValor.setText("");
        }
    }//GEN-LAST:event_btDeletarActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        JFrame frame = new JFrame();
        
        int resposta = JOptionPane.showConfirmDialog(frame,"Você tem certeza de que quer editar o Alimento?","Confirmação",JOptionPane.YES_NO_OPTION);
        
        if (resposta == JOptionPane.YES_OPTION) {
            int posicao = tbAlimento.getSelectedRow();

            lista.get(posicao).setNome(txtNome.getText());
            lista.get(posicao).setValor(Float.parseFloat(txtValor.getText()));

            DefaultTableModel modelo = (DefaultTableModel) tbAlimento.getModel();
            modelo.setNumRows(0);

            for (Alimento la: lista) {
                modelo.addRow(new Object[] {
                    la.getNome(),
                    la.getValor()
                });
            }

            txtNome.setText("");
            txtValor.setText("");
        } else {
            txtNome.setText("");
            txtValor.setText("");
        }
    }//GEN-LAST:event_btEditarActionPerformed

    private void tbAlimentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAlimentoMouseClicked
        int posicao = tbAlimento.getSelectedRow();
        
        txtNome.setText(tbAlimento.getValueAt(posicao, 0).toString());
        txtValor.setText(tbAlimento.getValueAt(posicao, 1).toString());
    }//GEN-LAST:event_tbAlimentoMouseClicked

